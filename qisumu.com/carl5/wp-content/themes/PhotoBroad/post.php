					<p>一直很想过很随性的日子。一直很想过很随性的日子。一直很想过很随性的日子。一直很想过很随性的日子。一直很想过很随性的日子。一直很想过很随性的日子。</p>
					<p>开心就笑，难过就哭。找个肩膀靠一靠，躲在背后挡一挡。沐浴在阳光下，然后奔跑。夜光下的踏马路。一群人在花地里继续花痴。蹦蹦跳跳过过小桥。蓝天白云，海滩花地，小桥流水。</p>
					<p>很做的事情很多，能做的事情却很少想要的很多，现在拥有的却不多。过去那些长不大的梦想，一个个在现在就破碎。各个城市，各奔东西，他的她的他的她。能够陪伴，也是一种幸福。非主流日志有时候，不是不确定感觉不是不确定自己想要的是什么，而是不确定那个人是不是和自己一样确定。更多的时候，不是需要时间，而是需要一种默契。</p>
